package ehu.es.cs19;

public class Letras {


  /**
   * The main method.
   *
   * @param args the arguments
   */
  public static void main(String[] args) {
    int marcaInt = (int) 'a';
    System.out.println(marcaInt);
    marcaInt = (int) 'A';
    System.out.println(marcaInt);
    marcaInt = (int) 'z';
    System.out.println(marcaInt);
    marcaInt = (int) 'Z';
    System.out.println(marcaInt);
    char letra = (char) 91;
    System.out.println(letra);

  }

}
